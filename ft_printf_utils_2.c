#include "ft_printf.h"

/*
**------------------------------------------------------------------------------
** FORMAT: %d - decimal
**------------------------------------------------------------------------------
*/

int	ft_decimal(long long n)
{
	int	i;

	i = 0;
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
		i++;
	}
	if (n >= 10)
	{
		i += ft_decimal(n / 10);
		i += ft_putchar(n % 10 + 48);
		return (i);
	}
	i += ft_putchar(n + 48);
	return (i);
}

/*
**------------------------------------------------------------------------------
** FORMAT: %d - unsigned decimal
**------------------------------------------------------------------------------
*/

int	ft_unsigned_decimal(unsigned long long n)
{
	int	i;

	i = 0;
	if (n >= 10)
	{
		i += ft_decimal(n / 10);
		i += ft_putchar(n % 10 + 48);
		return (i);
	}
	i += ft_putchar(n + 48);
	return (i);
}

/*
**------------------------------------------------------------------------------
** FORMAT: %x - hex lower; %X - hex upper
**------------------------------------------------------------------------------
*/

int	ft_hex(unsigned int n, int type)
{
	int		i;
	char	*hex;

	i = 1;
	hex = "0123456789abcdef0123456789ABCDEF";
	if (n < 16)
		write(1, &hex[n + 16 * type], 1);
	else
	{
		i += ft_hex(n / 16, type);
		ft_hex(n % 16, type);
	}
	return (i);
}
