#include "ft_printf.h"

/*
**------------------------------------------------------------------------------
** FORMAT: %c - char
**------------------------------------------------------------------------------
*/

int	ft_putchar(int c)
{
	write (1, &c, 1);
	return (1);
}

/*
**------------------------------------------------------------------------------
** FORMAT: %s - string
**------------------------------------------------------------------------------
*/

int	ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (!s)
	{
		write (1, "(null)", 6);
		return (6);
	}
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i);
}

/*
**------------------------------------------------------------------------------
** FORMAT: %p - pointer adress
**------------------------------------------------------------------------------
*/

int	ft_pointer_adress2(unsigned long p)
{
	char	*nbr;
	int		i;

	i = 0;
	nbr = "0123456789abcdef0123456789ABCDEF";
	if (p < 16)
	{
		write(1, &nbr[p], 1);
		i++;
	}
	else
	{
		i += ft_pointer_adress2(p / 16);
		i += ft_pointer_adress2(p % 16);
	}
	return (i);
}

int	ft_pointer_adress(unsigned long p)
{
	write (1, "0x", 2);
	return ((ft_pointer_adress2(p)) + 2);
}
